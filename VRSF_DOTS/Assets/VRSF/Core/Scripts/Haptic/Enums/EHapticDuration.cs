namespace VRSF.Core.Haptic
{
    /// <summary>
    /// How long to trigger the haptic event for in seconds
    /// </summary>
    public enum EHapticDuration
    {
        /// <summary>
        /// Correspond to 0.25 seconds
        /// </summary>
        SHORT,

        /// <summary>
        /// Correspond to 1.0 seconds
        /// </summary>
        MEDIUM,

        /// <summary>
        /// Correspond to 2.0 seconds
        /// </summary>
        LONG
    }
}